/*
 * myprogram.c — создание разреженных (sparse) файлов.
 *
 * Принцип работы:
 *   Программа читает входной поток поблочно и записывает данные в выходной файл.
 *   Блоки, целиком заполненные нулевыми байтами, на диск не записываются —
 *   вместо этого позиция в выходном файле сдвигается вперёд вызовом lseek().
 *   Такой приём создаёт "дыры" в файле: операционная система не
 *   выделяет под них дисковые блоки, но при чтении возвращает нулевые байты.
 *   В конце работы ftruncate() выставляет правильный логический размер файла,
 *   чтобы хвостовая дыра (если файл заканчивается нулями) не была потеряна.
 *
 * Использование:
 *   ./myprogram [-b размер_блока] [входной_файл] выходной_файл
 *
 *   Один файловый аргумент  — читать stdin, писать в выходной_файл.
 *   Два файловых аргумента  — читать входной_файл, писать в выходной_файл.
 *   -b размер_блока         — размер блока обработки в байтах (по умолчанию 4096).
 */

#define _POSIX_C_SOURCE 200809L  /* включает ftruncate() и прочие POSIX-расширения */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <fcntl.h>
#include <unistd.h>
#include <errno.h>
#include <getopt.h>
#include <sys/types.h>

/* Размер блока обработки по умолчанию. */
#define DEFAULT_BLOCK_SIZE 4096

/*
 * usage() — выводит краткую справку об использовании программы в stderr.
 *
 * Параметры:
 *   prog — имя исполняемого файла (argv[0]), подставляется в текст справки.
 *
 * Вызывается при неверных аргументах командной строки, после чего
 * вызывающий код завершает программу с кодом EXIT_FAILURE.
 */
static void usage(const char *prog) {
    fprintf(stderr, "Использование: %s [-b размер_блока] [входной_файл] выходной_файл\n", prog);
    fprintf(stderr, "  -b размер_блока   Размер блока в байтах (по умолчанию: %d)\n", DEFAULT_BLOCK_SIZE);
    fprintf(stderr, "  Один аргумент-файл  : читает stdin, пишет в выходной_файл\n");
    fprintf(stderr, "  Два аргумента-файла : читает входной_файл, пишет в выходной_файл\n");
}

/*
 * is_zero_block() — проверяет, содержит ли буфер только нулевые байты.
 *
 * Параметры:
 *   buf — указатель на начало буфера для проверки.
 *   len — количество байт для проверки (фактически прочитанных из read()).
 *
 * Возвращает:
 *   1 — все байты в диапазоне [0, len) равны нулю (блок можно пропустить
 *       через lseek, не записывая на диск);
 *   0 — найден хотя бы один ненулевой байт (блок нужно записать).
 *
 * Функция намеренно принимает ssize_t, а не size_t, чтобы тип совпадал
 * с возвращаемым значением read() и не требовал явного приведения типов.
 */
static int is_zero_block(const unsigned char *buf, ssize_t len) {
    for (ssize_t i = 0; i < len; i++) {
        if (buf[i] != 0) return 0;
    }
    return 1;
}

int main(int argc, char *argv[]) {
    int block_size = DEFAULT_BLOCK_SIZE;
    int opt; /* текущая опция, возвращаемая getopt() */

    /*
     * Разбор опций командной строки с помощью getopt().
     * Строка "b:" означает: опция -b принимает обязательный аргумент (двоеточие).
     * getopt() возвращает -1, когда все опции разобраны.
     */
    while ((opt = getopt(argc, argv, "b:")) != -1) {
        switch (opt) {
            case 'b':
                /*
                 * optarg — глобальная переменная getopt, указывает на строку-аргумент
                 * текущей опции (в данном случае — значение размера блока).
                 * atoi() преобразует строку в int; возвращает 0 при ошибке разбора,
                 * поэтому дополнительно проверяем, что результат положительный.
                 */
                block_size = atoi(optarg);
                if (block_size <= 0) {
                    fprintf(stderr, "Ошибка: размер блока должен быть положительным целым числом\n");
                    exit(EXIT_FAILURE);
                }
                break;
            default:
                /* Неизвестная опция — getopt() уже вывел сообщение об ошибке. */
                usage(argv[0]);
                exit(EXIT_FAILURE);
        }
    }

    /*
     * После разбора опций getopt() устанавливает optind — индекс первого
     * позиционного (не-опционального) аргумента в argv[].
     * remaining — количество оставшихся позиционных аргументов:
     *   1 → только выходной файл (входные данные берутся из stdin)
     *   2 → входной файл и выходной файл
     */
    int remaining = argc - optind;
    if (remaining < 1 || remaining > 2) {
        usage(argv[0]);
        exit(EXIT_FAILURE);
    }

    /*
     * fd_in  — файловый дескриптор источника данных.
     *          Инициализируется как STDIN_FILENO (= 0), чтобы по умолчанию
     *          читать стандартный ввод без явного открытия файла.
     * fd_out — файловый дескриптор выходного файла; открывается ниже.
     * out_path — путь к выходному файлу.
     */
    int fd_in = STDIN_FILENO;
    const char *out_path = NULL;

    if (remaining == 1) {
        /* Один позиционный аргумент: источник — stdin, приёмник — указанный файл. */
        out_path = argv[optind];
    } else {
        /* Два позиционных аргумента: первый — входной файл, второй — выходной. */
        const char *in_path = argv[optind];
        out_path = argv[optind + 1];

        
        fd_in = open(in_path, O_RDONLY);
        if (fd_in < 0) {
            fprintf(stderr, "Ошибка открытия входного файла '%s': %s\n", in_path, strerror(errno));
            exit(EXIT_FAILURE);
        }
    }

    /*
     * Открываем выходной файл для записи:
     *   O_WRONLY — только запись
     *   O_CREAT  — создать, если не существует
     *   O_TRUNC  — обнулить содержимое, если файл уже существует
     *   0644     — права доступа: владелец rw, группа r, остальные r
     */
    int fd_out = open(out_path, O_WRONLY | O_CREAT | O_TRUNC, 0644);
    if (fd_out < 0) {
        fprintf(stderr, "Ошибка открытия выходного файла '%s': %s\n", out_path, strerror(errno));
        /* Закрываем fd_in, только если он был явно открыт (не stdin). */
        if (fd_in != STDIN_FILENO) close(fd_in);
        exit(EXIT_FAILURE);
    }

    /*
     * Выделяем буфер для блочного чтения данных.
     * malloc() возвращает NULL при нехватке памяти.
     * Размер буфера совпадает с block_size, что позволяет читать ровно
     * один блок за вызов read() и сразу принимать решение: писать или пропускать.
     */
    unsigned char *buf = malloc(block_size);
    if (!buf) {
        fprintf(stderr, "Ошибка: не удалось выделить буфер размером %d байт\n", block_size);
        close(fd_in);
        close(fd_out);
        exit(EXIT_FAILURE);
    }

    /*
     * total_size — суммарное количество байт, прочитанных из источника.
     *   Используется в конце для вызова ftruncate(), чтобы выставить
     *   правильный логический размер выходного файла.
     *   Тип off_t (знаковое 64-битное целое на 64-битных системах) позволяет
     *   работать с файлами размером до 8 ЭиБ.
     *
     * pending_zeros — количество нулевых байт, накопленных с момента последней
     *   фактической записи, но ещё не "пропущенных" через lseek().
     *   Накопление позволяет объединить несколько подряд идущих нулевых блоков
     *   в один вызов lseek() вместо многократных прыжков.
     */
    off_t total_size    = 0;
    off_t pending_zeros = 0;

    /* Основной цикл обработки: читаем блоками до конца файла. */
    while (1) {
        /*
         * read() читает до block_size байт из fd_in в buf.
         * Возвращает:
         *   > 0  — фактическое количество прочитанных байт (может быть меньше
         *          block_size в конце файла или при чтении из pipe);
         *     0  — достигнут конец файла (EOF);
         *    -1  — ошибка чтения, подробности в errno.
         */
        ssize_t bytes_read = read(fd_in, buf, block_size);
        if (bytes_read < 0) {
            fprintf(stderr, "Ошибка чтения входных данных: %s\n", strerror(errno));
            free(buf);
            close(fd_in);
            close(fd_out);
            exit(EXIT_FAILURE);
        }
        if (bytes_read == 0) {
            /* Конец файла — выходим из цикла для завершающей обработки. */
            break;
        }

        /* Учитываем прочитанные байты в общем логическом размере. */
        total_size += bytes_read;

        if (is_zero_block(buf, bytes_read)) {
            /*
             * Весь блок — нули. Накапливаем их размер в pending_zeros.
             * На диск ничего не пишем: реальная дыра в выходном файле
             * будет создана позже, при первой же записи ненулевого блока
             * (или через ftruncate в самом конце, если нули хвостовые).
             */
            pending_zeros += bytes_read;
        } else {
            /*
             * Блок содержит хотя бы один ненулевой байт.
             * Сначала "перепрыгиваем" через все накопленные нули:
             * lseek(fd_out, pending_zeros, SEEK_CUR) сдвигает текущую позицию
             * записи на pending_zeros байт вперёд, не записывая на диск ничего.
             * Таким образом создается последовательность нулевых байт
             */
            if (pending_zeros > 0) {
                if (lseek(fd_out, pending_zeros, SEEK_CUR) == (off_t)-1) {
                    fprintf(stderr, "Ошибка seek в выходном файле: %s\n", strerror(errno));
                    free(buf);
                    close(fd_in);
                    close(fd_out);
                    exit(EXIT_FAILURE);
                }
                pending_zeros = 0; /* сбрасываем счётчик после прыжка */
            }

            /*
             * Записываем ненулевой блок в выходной файл.
             * write() может записать меньше байт, чем запрошено (частичная запись),
             * поэтому используем внутренний цикл с переменной written,
             * отслеживающей, сколько байт уже записано, пока не запишем всё.
             */
            ssize_t written = 0;
            while (written < bytes_read) {
                ssize_t w = write(fd_out, buf + written, bytes_read - written);
                if (w < 0) {
                    fprintf(stderr, "Ошибка записи в выходной файл: %s\n", strerror(errno));
                    free(buf);
                    close(fd_in);
                    close(fd_out);
                    exit(EXIT_FAILURE);
                }
                written += w;
            }
        }
    }

    /*
     * Завершающий шаг: устанавливаем правильный логический размер выходного файла.
     *
     * Зачем это нужно:
     *   Если входные данные заканчиваются нулями (pending_zeros > 0),
     *   последний вызов lseek() так и не был сделан — дыра в хвосте файла
     *   не создана, и текущая позиция записи не достигла конца.
     *   ftruncate(fd_out, total_size) устанавливает размер файла равным
     *   total_size байт: если файл короче — он расширяется нулями (дыра),
     *   если длиннее — усекается. Это единственный надёжный способ
     *   сохранить хвостовую дыру без записи нулевых байт на диск.
     *
     * Проверка total_size > 0 защищает от вызова ftruncate на пустом файле,
     * хотя технически это безвредно.
     */
    if (total_size > 0) {
        if (ftruncate(fd_out, total_size) < 0) {
            fprintf(stderr, "Ошибка установки размера выходного файла: %s\n", strerror(errno));
            free(buf);
            close(fd_in);
            close(fd_out);
            exit(EXIT_FAILURE);
        }
    }

    /* Освобождаем ресурсы: память и файловые дескрипторы. */
    free(buf);
    if (fd_in != STDIN_FILENO) close(fd_in);
    close(fd_out);

    return EXIT_SUCCESS;
}