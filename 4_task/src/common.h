/*
 * common.h — общие константы и утилиты для клиента и сервера.
 *
 * Протокол:
 *   - Транспорт: UNIX stream socket (SOCK_STREAM).
 *   - Сообщения — строки чисел в десятичной записи, разделённые '\n'.
 *   - Каждая строка не длиннее MAX_LINE_LEN символов (включая '\n').
 *   - Сервер на каждое полученное число отправляет в ответ строку
 *     с новым значением состояния и '\n' в конце.
 */

#ifndef BROWNIAN_COMMON_H
#define BROWNIAN_COMMON_H

#include <stddef.h>

/* Максимальная длина одной строки запроса/ответа.
 * По умолчанию строка не длиннее 10 символов;
 * для ответов сервера (накопленное состояние может быть большим)
 * берём с запасом. */
#define MAX_LINE_LEN        64

/* Размер буфера приёма на одного клиента.
 * Должен быть строго больше MAX_LINE_LEN, чтобы помещалась
 * хотя бы одна полная строка плюс хвост следующей. */
#define CLIENT_RECV_BUF     512

/* Размер буфера исходящих данных на одного клиента.
 * Если клиент медленно читает ответы, мы складываем их сюда. */
#define CLIENT_SEND_BUF     4096

/* Имя файла конфигурации по умолчанию (в текущем каталоге). */
#define DEFAULT_CONFIG      "config"

/* Где лежит сокет, если в config ничего не задано. */
#define DEFAULT_SOCKET_PATH "/tmp/brownian_bot.sock"

#endif
