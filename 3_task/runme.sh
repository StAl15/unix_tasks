#!/bin/bash
# runme.sh — сборка, запуск myinit и полный тестовый сценарий
# Результат тестов пишется в result.txt

set -e

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
MYINIT="$SCRIPT_DIR/myinit"
RESULT="$SCRIPT_DIR/result.txt"
LOG="/tmp/myinit.log"

# Рабочие каталоги (абсолютные пути — обязательное требование)
WORK_DIR="/tmp/myinit_test"
CONF1="$WORK_DIR/myinit.conf"
CONF2="$WORK_DIR/myinit_one.conf"
WORKER="$SCRIPT_DIR/worker.sh"

# ── цвета для терминала (не идут в result.txt) ─────────────────────────
GREEN='\033[0;32m'; RED='\033[0;31m'; NC='\033[0m'

pass() { echo -e "${GREEN}[PASS]${NC} $*"; }
fail() { echo -e "${RED}[FAIL]${NC} $*"; }

# ══════════════════════════════════════════════════════════════════════
# 0. Сборка
# ══════════════════════════════════════════════════════════════════════
echo "=== Сборка myinit ==="
cd "$SCRIPT_DIR"
make clean >/dev/null 2>&1 || true
make
echo "Сборка успешна."
echo

# Убедимся, что worker.sh исполняем
chmod +x "$WORKER"

# ══════════════════════════════════════════════════════════════════════
# 1. Подготовка окружения
# ══════════════════════════════════════════════════════════════════════
echo "=== Подготовка тестового окружения ==="
mkdir -p "$WORK_DIR"

# Создаём stdin-файлы (пустые, но существующие)
touch "$WORK_DIR/in1" "$WORK_DIR/in2" "$WORK_DIR/in3" "$WORK_DIR/in_one"

# ── конфиг 1: три процесса ─────────────────────────────────────────
cat > "$CONF1" <<EOF
/bin/bash $WORKER worker1 $WORK_DIR/in1 $WORK_DIR/out1
/bin/bash $WORKER worker2 $WORK_DIR/in2 $WORK_DIR/out2
/bin/bash $WORKER worker3 $WORK_DIR/in3 $WORK_DIR/out3
EOF

# ── конфиг 2: один процесс ─────────────────────────────────────────
cat > "$CONF2" <<EOF
/bin/bash $WORKER worker_one $WORK_DIR/in_one $WORK_DIR/out_one
EOF

# Убиваем старый myinit, если остался с прошлого запуска
pkill -x myinit 2>/dev/null || true
sleep 0.3
# Очищаем лог
rm -f "$LOG"

echo "Окружение готово."
echo

# ══════════════════════════════════════════════════════════════════════
# 2. Инициализация result.txt
# ══════════════════════════════════════════════════════════════════════
{
echo "======================================================"
echo "  ОТЧЁТ ПО ТЕСТИРОВАНИЮ myinit"
echo "  Дата: $(date)"
echo "======================================================"
echo
echo "ПРОГРАММА: myinit — упрощённый аналог init"
echo "Запускает дочерние процессы из конфига, демонизируется,"
echo "следит за потомками (перезапускает упавшие), по SIGHUP"
echo "перечитывает конфиг и рестартует все процессы."
echo
} > "$RESULT"

# ══════════════════════════════════════════════════════════════════════
# Вспомогательная функция: считать worker-процессы
# ══════════════════════════════════════════════════════════════════════
count_workers() {
    # считаем bash-процессы, запущенные через наш worker.sh
    pgrep -af "bash.*worker" 2>/dev/null | grep -v "$$" | grep -v "runme" | wc -l
}

# ══════════════════════════════════════════════════════════════════════
# ТЕСТ 1: Запуск трёх дочерних процессов
# ══════════════════════════════════════════════════════════════════════
{
echo "------------------------------------------------------"
echo "ТЕСТ 1: Старт myinit с тремя рабочими процессами"
echo "Ожидание: ps покажет ровно 3 дочерних worker-процесса"
echo "------------------------------------------------------"
} >> "$RESULT"

echo "=== ТЕСТ 1: запуск myinit с 3 процессами ==="
"$MYINIT" -c "$CONF1"
sleep 2   # ждём демонизации и старта потомков

N=$(count_workers)
PS_OUT=$(ps aux | grep -E "worker[123]" | grep -v grep | grep -v runme)

{
echo "Команда: ps aux | grep worker"
echo "$PS_OUT"
echo
if [ "$N" -ge 3 ]; then
    echo "РЕЗУЛЬТАТ: PASS — найдено $N worker-процессов (ожидалось >= 3)"
else
    echo "РЕЗУЛЬТАТ: FAIL — найдено $N worker-процессов (ожидалось >= 3)"
fi
echo
} >> "$RESULT"

if [ "$N" -ge 3 ]; then
    pass "Тест 1: запущено $N worker-процессов"
else
    fail "Тест 1: запущено только $N worker-процессов"
fi

# ══════════════════════════════════════════════════════════════════════
# ТЕСТ 2: Убить процесс №2 — myinit должен его перезапустить
# ══════════════════════════════════════════════════════════════════════
{
echo "------------------------------------------------------"
echo "ТЕСТ 2: Убийство worker2 и автоматический перезапуск"
echo "Ожидание: после pkill worker2 и паузы 1 с снова 3 процесса"
echo "------------------------------------------------------"
} >> "$RESULT"

echo "=== ТЕСТ 2: убиваем worker2, ждём перезапуска ==="
pkill -f "worker2" 2>/dev/null || true
sleep 2   # ждём, пока myinit обнаружит завершение и рестартует

N2=$(count_workers)
PS_OUT2=$(ps aux | grep -E "worker[123]" | grep -v grep | grep -v runme)

{
echo "Действие: pkill -f worker2, пауза 2 с"
echo "Команда: ps aux | grep worker"
echo "$PS_OUT2"
echo
if [ "$N2" -ge 3 ]; then
    echo "РЕЗУЛЬТАТ: PASS — найдено $N2 worker-процессов (ожидалось >= 3)"
else
    echo "РЕЗУЛЬТАТ: FAIL — найдено $N2 worker-процессов (ожидалось >= 3)"
fi
echo
} >> "$RESULT"

if [ "$N2" -ge 3 ]; then
    pass "Тест 2: worker2 перезапущен, итого $N2 процессов"
else
    fail "Тест 2: перезапуск не случился, процессов: $N2"
fi

# ══════════════════════════════════════════════════════════════════════
# ТЕСТ 3: Смена конфига + SIGHUP → только 1 процесс
# ══════════════════════════════════════════════════════════════════════
{
echo "------------------------------------------------------"
echo "ТЕСТ 3: SIGHUP с новым конфигом (1 процесс)"
echo "Ожидание: после замены конфига и kill -HUP запущен 1 процесс"
echo "------------------------------------------------------"
} >> "$RESULT"

echo "=== ТЕСТ 3: подменяем конфиг и шлём SIGHUP ==="
cp "$CONF2" "$CONF1"   # заменяем файл (myinit хранит абс. путь CONF1)
MYINIT_PID=$(pgrep -x myinit | head -1)

if [ -z "$MYINIT_PID" ]; then
    echo "ВНИМАНИЕ: myinit не найден, пропускаем ТЕСТ 3"
    echo "РЕЗУЛЬТАТ: SKIP — myinit не найден" >> "$RESULT"
else
    kill -HUP "$MYINIT_PID"
    sleep 2

    N3=$(count_workers)
    PS_OUT3=$(ps aux | grep -E "worker" | grep -v grep | grep -v runme)

    {
    echo "Действие: cp конфига с 1 процессом -> $CONF1"
    echo "         kill -HUP $MYINIT_PID, пауза 2 с"
    echo "Команда: ps aux | grep worker"
    echo "$PS_OUT3"
    echo
    if [ "$N3" -eq 1 ]; then
        echo "РЕЗУЛЬТАТ: PASS — найден ровно 1 worker-процесс"
    else
        echo "РЕЗУЛЬТАТ: FAIL — найдено $N3 worker-процессов (ожидался 1)"
    fi
    echo
    } >> "$RESULT"

    if [ "$N3" -eq 1 ]; then
        pass "Тест 3: после SIGHUP запущен ровно 1 процесс"
    else
        fail "Тест 3: после SIGHUP процессов: $N3 (ожидался 1)"
    fi
fi

# ══════════════════════════════════════════════════════════════════════
# ТЕСТ 4: Проверка лог-файла
# ══════════════════════════════════════════════════════════════════════
{
echo "------------------------------------------------------"
echo "ТЕСТ 4: Содержимое лог-файла $LOG"
echo "Ожидание: в логе есть записи о старте 3 процессов,"
echo "          завершении и рестарте worker2,"
echo "          получении SIGHUP, старте 1 нового процесса"
echo "------------------------------------------------------"
} >> "$RESULT"

echo "=== ТЕСТ 4: проверка лога ==="
sleep 0.5

if [ -f "$LOG" ]; then
    {
    echo "--- Содержимое $LOG ---"
    cat "$LOG"
    echo "--- Конец лога ---"
    echo
    } >> "$RESULT"

    # Проверяем ключевые события
    STARTS=$(grep -c "Started entry" "$LOG" 2>/dev/null || echo 0)
    RESTART=$(grep -c "restarting" "$LOG" 2>/dev/null || echo 0)
    HUP=$(grep -c "SIGHUP" "$LOG" 2>/dev/null || echo 0)
    TERM=$(grep -c "SIGTERM\|Sent SIGTERM" "$LOG" 2>/dev/null || echo 0)

    {
    echo "Анализ событий в логе:"
    echo "  Записей 'Started entry': $STARTS (ожидалось >= 4: 3 начальных + 1 после SIGHUP + рестарт)"
    echo "  Записей 'restarting':    $RESTART (ожидалось >= 1)"
    echo "  Записей 'SIGHUP':        $HUP     (ожидалось >= 1)"
    echo "  Записей 'SIGTERM':       $TERM    (ожидалось >= 3)"
    echo
    LOG_PASS=1
    [ "$STARTS" -ge 4 ] || { echo "  ! Мало записей о старте"; LOG_PASS=0; }
    [ "$RESTART" -ge 1 ] || { echo "  ! Нет записи о рестарте"; LOG_PASS=0; }
    [ "$HUP" -ge 1 ]     || { echo "  ! Нет записи о SIGHUP";  LOG_PASS=0; }
    [ "$TERM" -ge 3 ]    || { echo "  ! Мало записей SIGTERM"; LOG_PASS=0; }
    if [ "$LOG_PASS" -eq 1 ]; then
        echo "РЕЗУЛЬТАТ: PASS — все ключевые события присутствуют в логе"
    else
        echo "РЕЗУЛЬТАТ: FAIL — некоторые ожидаемые события отсутствуют"
    fi
    echo
    } >> "$RESULT"

    if [ "$STARTS" -ge 4 ] && [ "$RESTART" -ge 1 ] && [ "$HUP" -ge 1 ]; then
        pass "Тест 4: лог содержит все ключевые события"
    else
        fail "Тест 4: в логе не хватает некоторых событий"
    fi
else
    echo "РЕЗУЛЬТАТ: FAIL — лог-файл $LOG не найден" >> "$RESULT"
    fail "Тест 4: лог-файл не найден"
fi

# ══════════════════════════════════════════════════════════════════════
# Финал
# ══════════════════════════════════════════════════════════════════════
{
echo "======================================================"
echo "  ИТОГ"
echo "======================================================"
echo "  Тест 1 (старт 3 процессов):        $(grep -A2 'ТЕСТ 1' "$RESULT" | grep РЕЗУЛЬТАТ | awk '{print $2}')"
echo "  Тест 2 (рестарт убитого):          $(grep -A2 'ТЕСТ 2' "$RESULT" | grep РЕЗУЛЬТАТ | awk '{print $2}')"
echo "  Тест 3 (SIGHUP, 1 процесс):        $(grep -A2 'ТЕСТ 3' "$RESULT" | grep РЕЗУЛЬТАТ | awk '{print $2}')"
echo "  Тест 4 (содержимое лога):          $(grep 'РЕЗУЛЬТАТ.*лог' "$RESULT" | tail -1 | awk '{print $2}')"
echo "======================================================"
} >> "$RESULT"

# Остановить myinit после тестов
pkill -x myinit 2>/dev/null || true

echo
echo "Тесты завершены. Отчёт: $RESULT"
echo "Лог демона:             $LOG"
